// ---------------------------------------------------------
// PROBLEM 01: চিৎকার করে বলা (Uppercase)
// গল্প: ইউজার তার নাম ছোট হাতের অক্ষরে লিখেছে "rahim", 
// কিন্তু আমাদের হেডলাইনের জন্য সেটা বড় হাতের "RAHIM" করতে হবে।
// ---------------------------------------------------------
let p1_name = "rahim";
console.log(p1_name.toUpperCase())

//? তোমার কোড নিচে লেখো: (toUpperCase ব্যবহার করো)




// ---------------------------------------------------------
// PROBLEM 02: অতিরিক্ত স্পেস দূর করা (Trim)
// গল্প: ইউজার পাসওয়ার্ড দেওয়ার সময় ভুল করে শুরুতে আর শেষে স্পেস দিয়েছে। 
// সেই স্পেসগুলো কেটে ফেলে দাও।
// ---------------------------------------------------------
let p2_password = "   superSecret123   ";
let p2_result = p2_password.trim();
console.log(p2_result)

//? তোমার কোড নিচে লেখো: (trim ব্যবহার করো)



// ---------------------------------------------------------
// PROBLEM 03: শব্দ খোঁজা (Includes)
// গল্প: একটি বড় প্যারাগ্রাফে "JavaScript" শব্দটি আছে কি না 
// সেটা চেক করো। (Result true অথবা false আসবে)
// ---------------------------------------------------------
let p3_text = "I love coding with JavaScript because it is fun.";
let p3_check = p3_text.includes("JavaScript");
console.log(p3_check)

//? তোমার কোড নিচে লেখো: (includes ব্যবহার করো)




// ---------------------------------------------------------
// PROBLEM 04: ভুল শব্দ ঠিক করা (Replace)
// গল্প: ক্লায়েন্ট ভুল করে "Html" লিখেছে, কিন্তু সেটা হবে "HTML"। 
// শব্দটা পরিবর্তন করে দাও।
// ---------------------------------------------------------
let p4_message = "I am learning Html now.";
let p4_fixed = p4_message.replace("Html", "HTML");
console.log(p4_fixed)

//? তোমার কোড নিচে লেখো: (replace ব্যবহার করো)





// ---------------------------------------------------------
// PROBLEM 05: লেখা কেটে ছোট করা (Slice)
// গল্প: ব্লগের টাইটেল অনেক বড়। তুমি শুধু প্রথম ১০টি অক্ষর কেটে নাও।
// (0 থেকে 10 পর্যন্ত)
// ---------------------------------------------------------
let p5_title = "JavaScript is the best language for web";
let p5_shortTitle = p5_title.slice(0, 10);
console.log(p5_shortTitle)

//? তোমার কোড নিচে লেখো: (slice ব্যবহার করো)




// ---------------------------------------------------------
// PROBLEM 06: শব্দগুলো আলাদা করা (Split)
// গল্প: একটি ফলের তালিকা আছে কমা (,) দিয়ে লেখা। 
// এটাকে ভেঙে একটা Array বানাও।
// ---------------------------------------------------------
let p6_fruits = "Mango,Banana,Apple,Orange";
let p6_array = p6_fruits.split(",");
console.log(p6_array)

//? তোমার কোড নিচে লেখো: (split ব্যবহার করো)




// ---------------------------------------------------------
// PROBLEM 07: ফাইলের নাম চেক করা (EndsWith)
// গল্প: ইউজার একটা ছবি আপলোড করেছে। চেক করো ফাইলের নামটা ".png" 
// দিয়ে শেষ হয়েছে কি না।
// ---------------------------------------------------------
let p7_file = "my-photo.png";
let p7_isImage = p7_file.endsWith(".png");
console.log(p7_isImage)

//? তোমার কোড নিচে লেখো: (endsWith ব্যবহার করো)





// ---------------------------------------------------------
// PROBLEM 08: ইমেইল ক্লিন করা (Chaining - Trim + Lowercase)
// গল্প: ইউজার ইমেইল দিয়েছে "  MYNAME@Gmail.com  "। 
// তোমাকে এক লাইনে স্পেস সরাতে হবে এবং সব ছোট হাতের করতে হবে।
// ---------------------------------------------------------
let p8_email = "  MYNAME@Gmail.com  ";
let p8_cleanEmail = p8_email.trim().toLocaleLowerCase();
console.log(p8_cleanEmail)

//? তোমার কোড নিচে লেখো: (trim এবং toLowerCase একসাথে চেইন করো)




// ---------------------------------------------------------
// PROBLEM 09: ডোমেইন নাম বের করা (Advanced Slice/Split)
// গল্প: "support@google.com" থেকে শুধু "@" এর পরের অংশটুকু 
// মানে "google.com" বের করতে হবে। (Split বা Slice যা খুশি ব্যবহার করো)
// ---------------------------------------------------------
let p9_fullEmail = "support@google.com";
let p9_domain = p9_fullEmail.slice(8);
console.log(p9_domain)

//? তোমার কোড নিচে লেখো:




// ---------------------------------------------------------
// PROBLEM 10: পজিশন বের করা (IndexOf)
// গল্প: নিচের বাক্যে "bug" শব্দটি কত নম্বর ইনডেক্সে আছে তা বের করো।
// ---------------------------------------------------------
let p10_log = "System error: heavy bug found in code";
